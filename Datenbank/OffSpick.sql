CREATE TABLE Contact(
	ID int(6) unsigned NOT NULL auto_increment PRIMARY KEY,
	Name varchar(40) NOT NULL DEFAULT '',
	Description varchar(40) NOT NULL DEFAULT '',
	Sort int(3) unsigned NOT NULL DEFAULT '0',
	ValidFrom datetime NOT NULL DEFAULT '2000-01-01',
	ValidTo datetime NOT NULL DEFAULT '2100-01-01'
)
ENGINE = INNODB;




CREATE TABLE ContactNumber(
	Id int(6) unsigned NOT NULL auto_increment PRIMARY KEY,
	ContactID int(6) unsigned NOT NULL DEFAULT '0',
	FOREIGN KEY (ContactID) REFERENCES Contact(ID),
	Phone varchar(17) NOT NULL DEFAULT '',
	ValidFrom datetime NOT NULL DEFAULT '2000-01-01',
	ValidTo datetime NOT NULL DEFAULT '2100-01-01'
)
ENGINE = INNODB;




CREATE TABLE ContactCategory(
	Id int(6) unsigned NOT NULL auto_increment PRIMARY KEY,
	ContactID int(6) unsigned NOT NULL DEFAULT '0',
	FOREIGN KEY (ContactID) REFERENCES Contact(ID),
	CategoryID int(6) unsigned NOT NULL DEFAULT '0',
	FOREIGN KEY (CategoryID) REFERENCES Category(ID)
)
ENGINE = INNODB;



